x = 5
y = 10

#Equality Operator (==):
print(x==y)

#Inequality Operator (!=):
print(x!=y)

# Greater than operator or less than operator
print(x<y)
print(x>y)

a = 10
b = 10

#Less Than or Equal To Operator (>=):
print(a<=b)
# Greater Than or Equal To Operator (>=) 
print(a>=b)

#logical

print(x<6 and y>6)
print(x<6 or y>6)
print(not(x<6 or y>6))

